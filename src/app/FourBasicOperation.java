package app;

public class FourBasicOperation {

	static double plus (double x, double y) {
		double result = x + y;
		return result;
	}
	
	static double minus (double x, double y) {
		double result = x - y;
		return result;
	}
	
	static double multiply (double x, double y) {
		double result = x * y;
		return result;
	}
	
	static double divide (double x, double y) {
		double result = x / y;
		return result;
	}
}
